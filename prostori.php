    <p><strong>PROSTORNE</strong><strong> </strong><strong>MOGUĆNOSTI</strong><strong> </strong></p>
<p>Dom kulture Studentski grad izgrađen je po projektu profesora Arhitektonskog fakulteta 
  u Beogradu, dr Milana Mitrovića. Za obavljanje delatnosti Dom kulture raspolaže  prostorom površine 5.000 m2. Dom kulture Studentski grad, u urbanom ambijentu naselja Studentski  grad, moderno je i funkcionalno arhitektonsko zdanje, koje sadrži komplekse  Upravne zgrade, Velike sale i Velike galerije, Biblioteke s čitaonicama i  Galerijom i Plato s Amfiteatrom i <br />
  Letnjom pozornicom.<strong> </strong></p>
<p><strong><br />
</strong><br />
  <strong>Upravna zgrada (978 </strong><strong>m</strong><strong>2</strong><strong>)</strong> <br />
  U Upravnoj zgradi se nalazi Mala sala (77 sedišta) za  tribinske programe, filmske i video projekcije, kamerne predstave,  konferencije; Bistro (50 mesta); Sala za sastanke (35 sedišta); 3 studija, 2  šminkernice, pozorišni klub i kancelarije.<br />
  </p>
