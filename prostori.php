  <strong>Biblioteka s čitaonicama (1.949 </strong><strong>m</strong><strong>2</strong><strong>)</strong><br />
  Biblioteka s čitaonicama je segment objekta Doma  kulture Studentski grad koji čine četiri zasebna, međusobno povezana, prostora  s 504 čitalačka mesta, u kojima je moguće, pored redovne aktivnosti,  organizovati tribine, okrugle stolove, razgovore i izložbe knjiga. Biblioteka s  čitaonicama i pratećim prostorom: Galerijom, kancelarijama i magacinom za  knjige, čini jedinstvenu prostornu celinu.<br />
  <strong><br />
  <br />
  Izložbeni prostori</strong><br />
  Galerija - Površina 92 m2, opremljena je izložbenim  panoima i postamentima. <br />
