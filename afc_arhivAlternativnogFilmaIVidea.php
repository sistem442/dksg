  <p><strong>ALTERNATIVE FILM ARHIV*</strong></p>

  <p>Sledeći  tradiciju negovanja alternativnog filma, začetu u nekadašnjem Akademskom kino  klubu, danajšnji Akademski filmski centar Doma kulture "Studentski grad" svoju  aktivnost ovog puta usmerava u ostvarenje jednog značajnog kulturnog projekta.  Osnivanje arhiva alternativnog filma, predstavlja, u izvesnom smislu, idejnu  sintezu dosadašnjeg rada Akadmeskog filmskog centra sagledanog kroz intenzivno  negovanje filmskog stvaralaštva i aktivnost na planu kritičke revalorizacije i  teorijskog definisanja ove vrste filmske produkcije. </p>
  <p>Iako  je i ranije ukazivano na vrednosti našeg alternativnog filma, dosadašnja  društvena briga za očuvanje njegovog postojećeg fonda nije bila i usaglašena sa  njegovim celokupnim umetničkim, estetskim i kulturnim značajem. Stvaran  uglavnom sredstvima i entuzijazmom zaljubljenika, jugoslovenski alternativni  film je ostao u potpunosti prepušten svojim stvaraocima – amaterima i kino  klubovima. Umetnički i proizvodno alternativan u odnosu na vladajući kod  profesinalne prdukcije, ovaj film je do sada bio na margini pažnje postojećih  ustanova za čuvanje filmskih dela. </p>
  <p>Osnivanje  Arhiva alterantivnog filma je zato najadekvatniji način stvarnog iskazivanja  svesti o umetničkoj i kulturnoj relevantnosti jugoslovenske alternativne  filmske prakse. U tom smislu i koncepciju Arhva treba razumeti kao jedno od  najcelishodnijih rešenja za očuvanje svih umetnički vrednih dela stovrenih u  našem alternativnom filmu. </p>
  <br />

  <p><strong>ALTERNATIVE  FILM ARHIV</strong></p>
 
  <p>Osnivač Arhiva  je Akademski filmski centar. Doma kulture "Studentski grad", u čijem sastavu  Arhiv deluje. </p>

  <p><strong>Oblici rada</strong></p>
 
  Arhiv prikuplja  i čuva značajna ostvarenja alternativne filmske i video produkcije.
  Prikuplja i čuva  dokumente, fotose i knjige vezane za alternativni film i video. 
  Izdaje  publikacije iz oblasti alternativnog filma i videa, kao i informacije o  filmskom fondu i svojim aktivnostima. 
  Priredjuje  projekcije u dvorani Alternativnog bioskopa.
  Autorima,  teoretičarima, kao i svim zainteresovanim omogućuje uvid u arhivirane filmove  putem video-tejpa.
  
  <p><strong>Način  prikupljanja i čuvanja filmova</strong></p>
 
 Arhiv prikuplja  i čuva filmove na više načina:<br />
  Kopira  originale, vlasništva autora i čuva ih kao arhivske kopije<br />
  Autori i klubovi  mogu dati Arhivu filma na čuvanje, zadržavajući pravo vlasništva.<br />
  Autori  zadržavaju autorska prava nad svojim filmovima.<br />
  Originali i  arhivske kopije se ne prikazuju<br />
  Filmovi se  čuvaju prema medjunarodnim standardima za arhiviranje filmova.<br />
  U zavisnosti od  finansijskih mogućnostni praviće se kopije za projekciju isključivo u Sali  Alternativnog bioskopa, koji ima 80 mesta i mogućnost projektovanja svih filmskih  formata i načina ozvučavanja.<br />
  Svi arhivirani  filmovi prebacuju se na video-tejp, čime se omogućuje svim zainteresovanima  uvid u fond Arhiva.</p>
  
  <p><strong>Savet Arhiva</strong></p>
  
  <p>Sastoji se od  autora i teoretičara Alternativnog filma. Predlaže filmove za koje smatra da su  značajni za arhiviranje. Članovi Saveta, pored samostalnog praćenja  alternativne filmske i video produkcije za svoju informaciju koriste i  ALTERNATIVE FILM FESTIVAL koji organizuje Akademski filmski centar Doma kulture  "Studentski grad". </p>
 
